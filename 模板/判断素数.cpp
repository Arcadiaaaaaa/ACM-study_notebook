#include <bits/stdc++.h>
using namespace std;

/*
    判断素数模板

    这一版参考常用竞赛写法：
    1. 1 不是素数
    2. 2 和 3 是素数
    3. 如果一个数能被 2 或 3 整除，那它一定不是素数
    4. 除了 2 和 3 以外，素数只可能出现在 6k - 1 或 6k + 1 的位置

    为什么可以每次 i += 6：
    - 任意整数都可以写成 6k, 6k+1, 6k+2, 6k+3, 6k+4, 6k+5
    - 其中：
      6k     一定能被 6 整除
      6k+2   一定能被 2 整除
      6k+3   一定能被 3 整除
      6k+4   一定能被 2 整除
    - 所以除了 2 和 3 以外，可能成为素数的只剩下 6k+1 和 6k+5
    - 而 6k+5 也就是下一组里的 6(k+1)-1，所以常写成检查 i 和 i+2

    这样比“从 2 一直试到 sqrt(n)”更快，属于判断素数的常用优化。
*/

bool check(int x) {
    // 1 不是素数，这是最先要特判的情况
    if (x == 1) return false;

    // 2 和 3 都是素数，直接返回 true
    if (x == 2 || x == 3) return true;

    // 如果能被 2 或 3 整除，就不可能是素数
    // 这里顺便把所有大于 2 的偶数都筛掉了
    if (x % 2 == 0 || x % 3 == 0) return false;

    // 从 5 开始检查，每次跳 6
    // 检查的两个位置分别是：
    // i     -> 6k - 1
    // i + 2 -> 6k + 1
    //
    // 这里只检查到 i * i <= x 即可：
    // 注意这里的等号不能漏。   
    // 例如 x = 49 时，7 * 7 == 49，如果写成 i * i < x，
    // 那么 i = 7 这一轮不会进入循环，就会把 49 错判成素数。
    // 如果 x 有一个大于 sqrt(x) 的因子，
    // 那么一定也会有一个小于等于 sqrt(x) 的配对因子
    for (int i = 5; i * i <= x; i += 6) {
        // 只要 x 能被其中一个候选因子整除
        // 就说明 x 不是素数
        if (x % i == 0 || x % (i + 2) == 0) return false;
    }

    // 如果所有可能的因子都试过了，仍然无法整除
    // 那么 x 就是素数
    return true;
}

int main() {
    /*
        下面是一个最小使用示例：
        输入一个整数 n，输出它是不是素数。

        如果你以后只想保留模板函数，
        可以把 main 函数删掉，只留下 check 即可。
    */

    int n;
    cin >> n;

    if (check(n)) {
        cout << "Yes\n";
    } else {
        cout << "No\n";
    }

    return 0;
}
